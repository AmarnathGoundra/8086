ASSUME CS: CODE, DS: DATA, ES: EXTRA
DATA SEGMENT
A DB 'WELCOMES$'
STRLEN EQU ($-A)
STREQU DB 'STRINGS ARE EQUAL $'
STRNEQU DB 'STRINGS ARE NOT EQUAL $'
DATA ENDS
EXTRA SEGMENT
B DB 'WELCOMES$'
EXTRA ENDS
CODE SEGMENT
START: MOV AX, DATA
	 MOV DS, AX
	 MOV AX, EXTRA
	 MOV ES, AX
	 MOV SI, OFFSET A
	 MOV DI, OFFSET B
	 CLD 
	 MOV CX, STRLEN
	 REPZ CMPSB
	 JZ FORW
	 MOV AH, 09H
	 MOV DX, OFFSET STRNEQU
	 INT 21H
	 JMP EXITP
FORW:  MOV AH, 09H
	 MOV DX, OFFSET STREQU
	 INT 21H
EXITP: MOV AH, 4CH
INT 03H 
CODE ENDS
END START