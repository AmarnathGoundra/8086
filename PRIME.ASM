ASSUME CS: CODE, DS: DATA
DATA SEGMENT
COUNT DB 100
RES DB ?
DATA ENDS
CODE SEGMENT 
START: MOV AX, DATA
	 MOV DS, AX
	 XOR AX, AX
	 MOV AL, COUNT
	 MOV BL,AL
	 INC AL
	 MOV CL, 02
	 DIV CL
	 MOV CL, AL
REPEAT: MOV AL, BL
	  XOR AH, AH
	  DIV CL
	  CMP AH, 00H
	  JZ NONPRIME
	  DEC CL
	  CMP CL, 01
	  JNZ REPEAT
	  MOV DL, 01
	  JMP LAST
NONPRIME: MOV DL, 00H
LAST:  MOV RES, DL
	 INT 03H
CODE ENDS
END START