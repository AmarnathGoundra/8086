ASSUME CS: CODE, DS: DATA
DATA SEGMENT
NUM DB 12H, 45H, 67H, 09H, 04H
COUNT EQU 05H
EVP DB ?
ODN DB ?
DATA ENDS
CODE SEGMENT
START: MOV AX, DATA
	 MOV DS, AX
	 MOV SI, OFFSET NUM
	 MOV CL, COUNT
REPEAT: MOV AL, [SI]
	  SHR AL, 01
	  //SHL AL, 01 [FOR POSITIVE OR NEGATIVE]
	  JNC EVENNUM
	  INC BH
	  JMP NEXT
EVENNUM: INC BL
NEXT: INC SI
	DEC CL
	JNZ REPEAT
	MOV EVP, BL
	MOV ODN, BH
INT 03H
CODE ENDS 
END START