ASSUME CS: CODE, DS: DATA, ES: EXTRA
DATA SEGMENT
A1 DB 'WELCOME$'
STRLEN EQU ($-A1)
DATA ENDS
EXTRA SEGMENT
B DB ?
EXTRA ENDS
CODE SEGMENT
START: MOV AX, DATA
	 MOV DS, AX
	 MOV AX, EXTRA
	 MOV ES, AX
	 MOV SI, OFFSET A1
	 MOV DI, OFFSET B
	 MOV CX, STRLEN-1
	 ADD DI, CX
	 MOV DL, '$'
	 MOV ES:[DI], DL
UP: DEC DI
    MOV AL, DS:[SI]
    MOV ES:[DI], AL
    INC SI
    DEC CX
    JNZ UP
INT 03H 
CODE ENDS
END START