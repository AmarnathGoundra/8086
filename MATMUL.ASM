ASSUME CS:CODE, DS: DATA
DATA SEGMENT
MAT1 DB 02H, 01H, 05H, 02H, 10H, 05H, 03H, 01H, 04H
MAT2 DB 08H, 07H, 01H, 04H, 02H, 07H, 02H, 03H, 05H
DIM EQU 03H
RMAT DW 09H DUP(?)
DATA ENDS
CODE SEGMENT
START: MOV AX, DATA
	 MOV DS, AX
	 MOV SI, OFFSET MAT1
	 MOV BX, OFFSET RMAT
	 MOV CH, DIM
NEXT_ROW: MOV DI, OFFSET MAT2
	    MOV CL, DIM
NEXT_COL: MOV DL, DIM
	    MOV BP, 0000H
	    MOV AX, 0000H
	    SAHF
NEXT_ELE: MOV AL, [SI]
	    MUL BYTE PTR [DI]
	    ADD BP, AX
	    INC SI
	    ADD DI, 03
	    DEC DL
	    JNZ NEXT_ELE
	    SUB DI, 08
	    SUB SI, 03
	    MOV [BX], BP
	    ADD BX, 02
	    DEC CL
	    JNZ NEXT_COL
	    ADD SI, 03
	    DEC CH
	    JNZ NEXT_ROW
     	    MOV AH, 4CH
INT 03H
CODE ENDS
END START