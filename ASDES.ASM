ASSUME CS: CODE, DS: DATA
DATA SEGMENT
NUM DW 0012H, 0045H, 0067H, 0009H, 0004H
# DB [FOR 8-BIT]
COUNT EQU 05H
DATA ENDS
CODE SEGMENT
START: MOV AX, DATA
	 MOV DS, AX
	 MOV DL, COUNT-1
UP: MOV CL, DL
    MOV SI, OFFSET NUM
REPEAT: MOV AX, [SI]
	  CMP AX, [SI+2]
	  JC NEXT
	  # JNC NEXT [FOR DESCENDING]
	  XCHG [SI+2], AX
	  XCHG [SI], AX
NEXT: INC SI
	INC SI
	LOOP REPEAT
	DEC DL
	JNZ UP
INT 03H
CODE ENDS 
END START