ASSUME CS:CODE, DS: DATA
DATA SEGMENT
MAT1 DB 09H, 08H, 07H, 06H, 05H, 04H, 03H, 02H, 01H
MAT2 DB 01H, 02H, 03H, 04H, 05H, 06H, 07H, 08H, 09H
DIM EQU 09H
RMAT DB ?
DATA ENDS
CODE SEGMENT
START: MOV AX, DATA
	 MOV DS, AX
	 MOV SI, OFFSET MAT1
	 MOV DI, OFFSET MAT2
	 MOV BX, OFFSET RMAT
	 MOV CX, DIM
NEXT:  XOR AX, AX
	 MOV AL, [SI]
	 ADD AL, [DI]
#	 SUB AL, [DI] (FOR SUBTRACTION)
	 MOV WORD PTR [BX], AX
	 INC SI
	 INC DI
	 ADD BX, 02
	 LOOP NEXT
	 MOV AH, 4CH
INT 03H
CODE ENDS
END START